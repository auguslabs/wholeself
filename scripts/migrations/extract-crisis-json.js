/**
 * Extract crisis-resources content from phpMyAdmin export JSON and write
 * crisis_resources_content.json + SQL seed 012.
 * Run: node scripts/migrations/extract-crisis-json.js [path-to-export.json]
 * Input default: %USERPROFILE%\Downloads\page_content-last-downloaded.json
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const exportPath = process.argv[2] || path.join(process.env.USERPROFILE || '', 'Downloads', 'page_content-last-downloaded.json');
const raw = fs.readFileSync(exportPath, 'utf8');

// phpMyAdmin export: array of { type, ... } or { type, name, data: [ rows ] }
let row;
try {
  const arr = JSON.parse(raw);
  const table = Array.isArray(arr) && arr.find((x) => x.type === 'table' && x.name === 'page_content');
  const data = table && table.data;
  if (Array.isArray(data)) {
    row = data.find((r) => r.page_id === 'crisis-resources');
  }
} catch (_) {}
if (!row || !row.content) {
  console.error('crisis-resources row not found or missing content in export');
  process.exit(1);
}
let content;
try {
  content = typeof row.content === 'string' ? JSON.parse(row.content) : row.content;
} catch (e) {
  console.error('Parse content error:', e.message);
  process.exit(1);
}
// seo comes from row.seo in export (separate column)
const seo = (typeof row.seo === 'string' ? (() => { try { return JSON.parse(row.seo); } catch (_) { return {}; } })() : row.seo) || {};
content.seo = seo;

const dataDir = path.join(__dirname, 'data');
fs.mkdirSync(dataDir, { recursive: true });
const outJson = path.join(dataDir, 'crisis_resources_content.json');
fs.writeFileSync(outJson, JSON.stringify(content, null, 2), 'utf8');
console.log('Written', outJson);
console.log('hero:', !!content.hero, 'button:', !!content.button, 'categories:', content.categories?.length);

// Build SQL: escape single quotes for MySQL (double them)
function sqlEscape(s) {
  if (s == null) return 'NULL';
  return "'" + String(s).replace(/\\/g, '\\\\').replace(/'/g, "''") + "'";
}
const categoriesJson = JSON.stringify(content.categories || []);
const seoData = content.seo || { title: { en: '', es: '' }, description: { en: '', es: '' } };
const hero = content.hero || {};
const button = content.button || {};

const rows = [
  {
    locale: 'en',
    seo_title: (seoData.title && seoData.title.en) || 'Crisis Resources - WholeSelf Counseling',
    seo_description: (seoData.description && seoData.description.en) || '24/7 crisis support resources',
    hero_title: (hero.title && hero.title.en) || 'Crisis Resources',
    button_aria_label: (button.ariaLabel && button.ariaLabel.en) || 'Open crisis resources',
    button_title: (button.title && button.title.en) || 'Crisis Resources',
    categories_json: categoriesJson,
  },
  {
    locale: 'es',
    seo_title: (seoData.title && seoData.title.es) || 'Recursos de Crisis - WholeSelf Counseling',
    seo_description: (seoData.description && seoData.description.es) || 'Recursos de apoyo en crisis 24/7',
    hero_title: (hero.title && hero.title.es) || 'Recursos de Crisis',
    button_aria_label: (button.ariaLabel && button.ariaLabel.es) || 'Abrir recursos de crisis',
    button_title: (button.title && button.title.es) || 'Recursos de Crisis',
    categories_json: categoriesJson,
  },
];

const sqlLines = [
  '-- Seed crisis-resources with content from page_content export.',
  '-- Run after 011_create_page_crisis_resources.sql (table must exist).',
  '-- Generated by: node scripts/migrations/extract-crisis-json.js',
  '',
];
for (const r of rows) {
  sqlLines.push(
    "UPDATE page_crisis_resources SET " +
    "meta_last_updated = NULL, meta_version = 1, " +
    "seo_title = " + sqlEscape(r.seo_title) + ", " +
    "seo_description = " + sqlEscape(r.seo_description) + ", " +
    "hero_title = " + sqlEscape(r.hero_title) + ", " +
    "button_aria_label = " + sqlEscape(r.button_aria_label) + ", " +
    "button_title = " + sqlEscape(r.button_title) + ", " +
    "categories_json = " + sqlEscape(r.categories_json) + " " +
    "WHERE locale = " + sqlEscape(r.locale) + ";"
  );
}
const sqlPath = path.join(__dirname, '012_seed_crisis_resources_from_export.sql');
fs.writeFileSync(sqlPath, sqlLines.join('\n'), 'utf8');
console.log('Written', sqlPath);
